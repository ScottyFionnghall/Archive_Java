/*
The moon’s gravity is about 17 percent that of earth’s. Write a program that
computes your effective weight on the moon.
*/
public class WeightOnTheMoon{
    public static void main(String[] args) {
        float EarthWeight = 90;
        System.out.println(EarthWeight + "kg on the moon will be "+ EarthWeight*0.17+"kg");
    }
}